# Basic Data Types

# Integer
number=20
# float
decimalNumber=10.2
# Boolean
choice=True
# None value
none=None

# print type of variable name
print (type(number),type(decimalNumber),type(choice),type(none))

# prints varible names
print (number,decimalNumber,choice,none)

# is a variable a type returns True or False
print (none is None)

# is not variable type returns Ture or False
print (number is not None)

# Math
# Comeparison operators works the same as any language
# Typical math operators: +,-,*,% works the same except:
# / returns a float
print (3/4)

# // returns an Integer
print (3//4)

# exponents
print (3**4)

# Integer and floats can be mixed in math operations
print (number + decimalNumber)
